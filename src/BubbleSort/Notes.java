package BubbleSort;

public class Notes {
	/*
	 * Bubble Sort /Exchange Sort /In-place sorting Algorithm (No need of new array) /Stable Sorting 
	 * 
	 * Current element will be compared to the adjacent element and will get swapped based on the comparison
	 * 
	 * 
	 * With every pass, the largest element will be moved to the last
	 * Pass 1-> Largest element in the array will be in the last
	 * Pass 2 -> Largest element in the remaining array will be moved to second from the last
	 * continues:
	 * 
	 * Space Complexity: O(1)
	 * Time Complexity: 
	 * Best case: O(N) -> Sorted Array
	 * Worst case: O(N2) -> Sorted in reverse order
	 * 
	 * 
	 * Note: When J never swaps for a value of i, it means the array is sorted. You can end the program.
	 * Hence the best comparison is N.
	 * 
	 * 
	 * Stable and unstable sorted algorithm
	 * Stable -> When the original order is maintained including the property (color) and value
	 * 
	 */
}
